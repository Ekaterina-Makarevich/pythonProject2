# Задание со звездочкой по строкам:
# Определить является ли строка палиндромом. Пример строки И лежу. — Ужели?.

import re
s = input('Введите строку:')
s = re.sub('[ |.|—|?]', '', s)
t = s.lower()
print(t)
# Выполняем срез и переворачиваем слово
if t == t[ : :-1]:
    print("Палиндром")
else:
    print("Не палиндром")